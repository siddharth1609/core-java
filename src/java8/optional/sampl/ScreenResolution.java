package java8.optional.sampl;

public class ScreenResolution {

	 private int width ;
	 private int height;
 
	public int getWidth() {
		return width;
	}
	public ScreenResolution(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	 
}
